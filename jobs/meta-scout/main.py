def ensure_labels() -> None:
    for name, color, description in LABELS:
        subprocess.run(
            [
                "gh",
                "label",
                "create",
                name,
                "--color",
                color,
                "--description",
                description,
                "--force",
                "--repo",
                REPO,
            ],
            capture_output=True,
        )


def title_tokens(title: str) -> set[str]:
    return set(title.lower().split())


def is_duplicate(new_title: str, existing_titles: list[str], threshold: float = 0.6) -> bool:
    new_tokens = title_tokens(new_title)
    if not new_tokens:
        return False
    for existing in existing_titles:
        existing_tokens = title_tokens(existing)
        if not existing_tokens:
            continue
        overlap = len(new_tokens & existing_tokens) / max(len(new_tokens), len(existing_tokens))
        if overlap > threshold:
            return True
    return False


def get_existing_issue_titles() -> list[str]:
    result = subprocess.run(
        [
            "gh",
            "issue",
            "list",
            "--state",
            "all",
            "--limit",
            "200",
            "--json",
            "title",
            "--repo",
            REPO,
        ],
        capture_output=True,
        text=True,
    )
    if result.returncode != 0:
        return []
    return [i["title"] for i in json.loads(result.stdout)]


