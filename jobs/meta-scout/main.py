def main() -> None:
    workspace = Path(os.environ.get("CLAUDE_P_WORKSPACE_DIR", "."))

    print("=== meta-scout: ensuring labels ===")
    ensure_labels()

    # Phase 1: brainstorm
    print("=== Phase 1: brainstorm ===")
    run_claude(
        prompt=PHASE1_PROMPT,
        allowed_tools=["Read", "Write", "Bash"],
        max_turns=25,
        max_budget_usd=1.50,
        cwd=workspace,
    )

    candidates_path = workspace / "candidates.json"
    if not candidates_path.exists():
        print("Phase 1 did not produce candidates.json", file=sys.stderr)
        sys.exit(1)

    raw = json.loads(candidates_path.read_text())
    n_candidates = len(raw.get("candidates", []))
    print(f"Phase 1 produced {n_candidates} candidates")

    # Phase 2: evaluate
    print("=== Phase 2: evaluate ===")
    run_claude(
        prompt=PHASE2_PROMPT,
        allowed_tools=["Read", "Write"],
        max_turns=10,
        max_budget_usd=0.75,
        cwd=workspace,
    )

    proposals_path = workspace / "proposals.json"
    if not proposals_path.exists():
        print("Phase 2 did not produce proposals.json", file=sys.stderr)
        sys.exit(1)

    proposals = ProposalsFile.model_validate(json.loads(proposals_path.read_text()))
    print(f"Phase 2: {len(proposals.proposals)} proposals, {len(proposals.rejected)} rejected")

    # Dedup against existing issues
    existing_titles = get_existing_issue_titles()
    created = 0
    skipped_dedup = 0

    for p in proposals.proposals:
        if is_duplicate(p.title, existing_titles):
            print(f"  SKIP (duplicate): {p.title}")
            skipped_dedup += 1
            continue

        body = (
            p.body
            + f"\n\n---\n_Scout evaluation: {p.evaluation.composite_score}/15 "
            + f"(V:{p.evaluation.value} F:{p.evaluation.feasibility} "
            + f"A:{p.evaluation.alignment})_\n"
            + f"_Rationale: {p.evaluation.rationale}_"
        )

        label_args: list[str] = []
        for label in p.labels:
            label_args.extend(["--label", label])

        result = subprocess.run(
            [
                "gh",
                "issue",
                "create",
                "--title",
                p.title,
                "--body",
                body,
                *label_args,
                "--repo",
                REPO,
            ],
            capture_output=True,
            text=True,
        )
        if result.returncode == 0:
            print(f"  CREATED: {p.title} → {result.stdout.strip()}")
            existing_titles.append(p.title)
            created += 1
        else:
            print(f"  FAILED: {p.title}: {result.stderr}", file=sys.stderr)

    print(f"\n=== Done: {created} issues created, {skipped_dedup} skipped (dedup) ===")


if __name__ == "__main__":
    main()
