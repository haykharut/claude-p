PHASE2_PROMPT = f"""\
You are a rigorous technical reviewer. Your job is to evaluate a set of feature
proposals for the claude-p project and select only the strongest ones.

## Input

Read the file candidates.json in the current directory. It contains raw ideas
generated by a brainstorming pass over the claude-p codebase.

## Evaluation criteria

Score each candidate on a 1–5 scale for each criterion:

1. **Value** (1–5): How much does this improve the project for its target user
   (solo hacker with a home server and Claude subscription)?
2. **Feasibility** (1–5): Can this be implemented correctly in 1–3 days by an
   AI coding agent working alone? Consider: complexity, testing difficulty,
   risk of regressions, clarity of acceptance criteria.
3. **Alignment** (1–5): Does this fit the project's philosophy? Lightweight,
   single-user, no heavy infra, subscription-based Claude usage, filesystem
   as the source of truth.
4. **Not-already-done** (pass/fail): Is this NOT already in CHANGELOG
   [Unreleased], NOT in existing GitHub issues, NOT a duplicate of another
   candidate in this batch?
5. **Not-out-of-scope** (pass/fail): Is this NOT in the explicit out-of-scope
   list below?

The out-of-scope list (reject any idea that overlaps with these):
{OUT_OF_SCOPE}

## Selection rules

- REJECT any candidate that fails "not-already-done" or "not-out-of-scope"
- REJECT any candidate with Value < 3 OR Feasibility < 3 OR Alignment < 3
- Compute composite_score = Value + Feasibility + Alignment (max 15)
- Keep the top 3–5 survivors ranked by composite_score
- If fewer than 3 survive, that is fine — quality over quantity

## Output format

Write a file called proposals.json in the current directory:

{{
  "generated_at": "<ISO datetime>",
  "proposals": [
    {{
      "title": "<GitHub issue title — imperative mood, concise>",
      "body": "<Full GitHub issue body in markdown. Sections: Problem, Proposed Solution, Files to Change, Acceptance Criteria>",
      "labels": ["scout-proposal", "enhancement"],
      "evaluation": {{
        "value": 4,
        "feasibility": 5,
        "alignment": 4,
        "composite_score": 13,
        "rationale": "<1-2 sentences on why this survived the filter>"
      }}
    }}
  ],
  "rejected": [
    {{
      "title": "<original candidate title>",
      "reason": "<specific reason for rejection>"
    }}
  ]
}}

Be ruthless. Every proposal becomes a real GitHub issue that a human reviews.
A short list of high-confidence proposals is far better than a long list of
maybes. If you are unsure about a candidate, reject it.
"""


# ---------------------------------------------------------------------------
# Pydantic models for proposals.json validation
# ---------------------------------------------------------------------------


class Evaluation(BaseModel):
    model_config = ConfigDict(extra="forbid")
    value: int
    feasibility: int
    alignment: int
    composite_score: int
    rationale: str


class Proposal(BaseModel):
    model_config = ConfigDict(extra="forbid")
    title: str
    body: str
    labels: list[str]
    evaluation: Evaluation


class Rejected(BaseModel):
    model_config = ConfigDict(extra="forbid")
    title: str
    reason: str


class ProposalsFile(BaseModel):
    model_config = ConfigDict(extra="forbid")
    generated_at: str
    proposals: list[Proposal]
    rejected: list[Rejected]


# ---------------------------------------------------------------------------
# Claude CLI helper
# ---------------------------------------------------------------------------


def run_claude(
    prompt: str,
    *,
    allowed_tools: list[str],
    max_turns: int,
    max_budget_usd: float,
    cwd: Path,
) -> dict:
    """Shell out to claude -p and return the parsed result envelope."""
    claude_cli = os.environ.get("CLAUDE_P_CLAUDE_CLI", "claude")
    argv = [
        claude_cli,
        "-p",
        "--output-format",
        "json",
        "--verbose",
        "--permission-mode",
        "dontAsk",
        "--allowedTools",
        ",".join(allowed_tools),
        "--max-budget-usd",
        f"{max_budget_usd:g}",
        "--max-turns",
        str(max_turns),
        prompt,
    ]
    result = subprocess.run(argv, capture_output=True, text=True, cwd=cwd)
    if result.returncode != 0:
        print(f"claude -p failed (exit {result.returncode})", file=sys.stderr)
        print(result.stderr[-3000:], file=sys.stderr)
        sys.exit(1)

    envelope = json.loads(result.stdout)
    cost = float(envelope.get("cost_usd") or envelope.get("total_cost_usd") or 0)
    turns = int(envelope.get("num_turns") or 0)
    is_error = bool(envelope.get("is_error"))
    print(f"  cost=${cost:.2f}, turns={turns}, error={is_error}")

    _report_to_ledger(envelope)
    return envelope


def _report_to_ledger(envelope: dict) -> None:
    run_id = os.environ.get("CLAUDE_P_RUN_ID")
    job_dir = os.environ.get("CLAUDE_P_JOB_DIR")
    if not run_id or not job_dir:
        return
    usage = envelope.get("usage") or {}
    if not isinstance(usage, dict):
        usage = {}
    record = {
        "cost_usd": float(envelope.get("cost_usd") or envelope.get("total_cost_usd") or 0),
        "input_tokens": int(usage.get("input_tokens") or 0),
        "output_tokens": int(usage.get("output_tokens") or 0),
        "cache_read_tokens": int(usage.get("cache_read_input_tokens") or 0),
        "cache_creation_tokens": int(usage.get("cache_creation_input_tokens") or 0),
        "num_turns": int(envelope.get("num_turns") or 0),
        "session_id": envelope.get("session_id"),
        "is_error": bool(envelope.get("is_error")),
        "model_usage": envelope.get("modelUsage"),
    }
    run_dir = Path(job_dir) / "runs" / run_id
    run_dir.mkdir(parents=True, exist_ok=True)
    with (run_dir / "claude_calls.jsonl").open("a") as f:
        f.write(json.dumps(record) + "\n")


